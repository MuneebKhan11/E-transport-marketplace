#include <iostream>
#include <cstring>
#include <iostream>

using namespace std;

/**
 * Encrypts a given input string.
 *
 * @param input The input string to encrypt.
 * @return string The encrypted string.
 */
string encryptString(const string &input);

/**
 * Decrypts a given input string.
 *
 * @param input The input string to decrypt.
 * @return string The decrypted string.
 */
string decryptString(const string &input);
